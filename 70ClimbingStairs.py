class Solution(object):
    def climbStairs(self, n):
        if n == 1:
            return 1
        if n == 2:
            return 2

        step_0, step_1 = 1, 2

        for i in range(2, n):
            next_step = step_0 + step_1
            step_0, step_1 = step_1, next_step
        return step_1


